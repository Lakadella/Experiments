package dcc;

public class Monster {

	private String name;
	private int atkBonus;
	private int dmgDie;
	private int amountDie;
	private int hp;
	private int hitDie;
	
	
	public Monster(String name, int atkBonus, int dmgDie, int amountDie, int hp, int hitDie) {
		this.name = name;
		this.atkBonus = atkBonus;
		this.dmgDie = dmgDie;
		this.amountDie = amountDie;
		this.hp = hp;
		this.hitDie = hitDie;
				
		
	}
	
	
}
